# string is a sequence of characters enclosed in quotes
# string is mutable, meaning it can be changed after it is created
# string is ordered, meaning the characters in a string have a specific order
# string allows duplicates, meaning the same character can appear multiple times in a string
# string is indexed, meaning each character in a string has a specific index
my_String="mit"
print(my_String)
print(type(my_String))
capitalized_string = my_String.capitalize() # this will capitalize the first letter of the string
print(capitalized_string)
uppercase_string = my_String.upper() # this will convert all the letters in the string to uppercase
print(uppercase_string)   
